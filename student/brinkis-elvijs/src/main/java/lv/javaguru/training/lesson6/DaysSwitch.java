package lv.javaguru.training.lesson6;

public class DaysSwitch {
    public static void main(String[] args) {
        System.out.println(getDay(5));
    }
    public static String getDay(int dayNumber) {
        String day;
        switch (dayNumber) {
            case 1: {
                day = "Monday";
                break;
            }
            case 2: {
                day = "Monday";
                break;
            }
            case 3: {
                day = "Wednesday";
                break;
            }

        }
        return null;

    }


}

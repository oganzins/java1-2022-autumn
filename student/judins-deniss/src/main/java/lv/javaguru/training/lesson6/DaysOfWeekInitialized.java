public class DaysOfWeekInitialized {

    public static void main(String[] args) {
        String[] daysOfWeek = {"Monday", "Tuesday","Wednesday", "Thursday", "Friday", "Saturday", "Sunday", "Day 8"};

        for (int i = 0; i < daysOfWeek.length; i++) {
            String day = daysOfWeek[i];
            System.out.println(day);
        }
    }
}

public class IncrementDecriment {
    public static void main(String[] args) {
        int i = 0;
        i = i + 1;

        System.out.println(i);

        int j = 0;
        j += 1;

        System.out.println(j);

        int k = 0;
        k++;

        System.out.println("k=" + k);


    }
}

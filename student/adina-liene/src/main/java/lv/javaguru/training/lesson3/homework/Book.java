package lv.javaguru.training.lesson3.homework;

/*
    Homework accepted.
 */
public class Book {

    String title;
    String author;
    int pageCount;


    String getTitle() {
        return this.title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    String getAuthor() {
        return this.author;
    }

    public void setAuthor(String author) {
        this.author = author;
    }


    int getPageCount() {
        return this.pageCount;
    }

    public void setPageCount(int pageCount) {
        this.pageCount = pageCount;
    }
}

